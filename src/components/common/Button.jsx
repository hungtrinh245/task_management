// src/components/common/Button.jsx
import { forwardRef } from "react";

const Button = forwardRef(
  (
    {
      children,
      variant = "primary",
      size = "md",
      icon: Icon,
      iconPosition = "left",
      isLoading = false,
      disabled = false,
      className = "",
      ...props
    },
    ref
  ) => {
    const variants = {
      primary:
        "bg-indigo-600 hover:bg-indigo-700 text-white shadow-lg hover:shadow-xl",
      secondary: "bg-gray-200 hover:bg-gray-300 text-gray-800",
      danger:
        "bg-red-600 hover:bg-red-700 text-white shadow-lg hover:shadow-xl",
      success:
        "bg-green-600 hover:bg-green-700 text-white shadow-lg hover:shadow-xl",
      outline: "border-2 border-gray-300 text-gray-700 hover:bg-gray-50",
    };

    const sizes = {
      sm: "px-3 py-2 text-sm gap-2",
      md: "px-4 py-3 text-base gap-2",
      lg: "px-6 py-4 text-lg gap-3",
      full: "w-full px-4 py-3 text-base gap-2",
    };

    const baseClasses =
      "inline-flex items-center justify-center font-semibold rounded-lg transition-all duration-200 disabled:opacity-50 disabled:cursor-not-allowed";

    return (
      <button
        ref={ref}
        disabled={disabled || isLoading}
        className={`${baseClasses} ${variants[variant]} ${sizes[size]} ${className}`}
        {...props}
      >
        {isLoading ? (
          <>
            <svg
              className="animate-spin h-5 w-5"
              xmlns="http://www.w3.org/2000/svg"
              fill="none"
              viewBox="0 0 24 24"
            >
              <circle
                className="opacity-25"
                cx="12"
                cy="12"
                r="10"
                stroke="currentColor"
                strokeWidth="4"
              ></circle>
              <path
                className="opacity-75"
                fill="currentColor"
                d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
              ></path>
            </svg>
            <span>Loading...</span>
          </>
        ) : (
          <>
            {Icon && iconPosition === "left" && <Icon className="w-5 h-5" />}
            <span>{children}</span>
            {Icon && iconPosition === "right" && <Icon className="w-5 h-5" />}
          </>
        )}
      </button>
    );
  }
);

Button.displayName = "Button";
export default Button;
